          <button class="accordian">January 3rd Egypt</button>
          <div class="panel">
            <p class="hand" style="text-align: right;">January 3rd. 1914.</p>
            <p class="hand">Having just received a letter from you dated Sept. 17th,  which I should have had a week previous to our departure from Enoggera, I will, methinks, at once proceed to answer it, although only last night I posted one to Dot. So consequently I will have very little news to tell you, which perhaps is just as well as I will not have much time to spare and may be called away at any moment for, you know, a soldiers time is not his own.</p>
            <p class="hand">This morning we had Church parade on the desert with a mound of sand for a pulpit and the parson preached a fair sermon with a text something about  -- “quit ye like men, be strong.”   Always after Church parade there is a march past of the whole Brigade and it is indeed a stirring sight;  there is generally 4,000 men and a large band.</p>
            <p class="hand">Yesterday I was on leave and got away about 2 p.m. with some other fellows.  We went by tram to the Zoo which is situated about five miles from the camp on the road to Cairo.  It is a great place and beats the Melbourne Zoo all to pieces.  There are lovely grottos, beautiful walks and grand trees and ferns everywhere. There are animals and birds and reptiles of all descriptions and here is a list of some of what we saw:- Animals:- Sheep with four horns, Giraffes – about a dozen, Zebras – three, Antelopes, dozens of all kinds,  Addax, Gazelle, White tailed Gnu, Water-buck, Wild Sheep, Nuer Ox, Bushbuck, Reedbuck, Jaguar, Lions, Lioness and cubs, Leopards, Hyenas, Jackall, Wolf, Fox, Goats, Syrian Bear and other kinds, Baboons, Mandrills and dozens of other monkeys, Jungle Cat, Genet Cat, African Cat, Wart  Hog – an ugly beast, Syrian Hyrax, Porcupines;   Reptiles, etc.:- Toads, Tortoises, many snakes, Horned Viper, Turtles, Weasel, Fruit-bat, Gerbilles, Golden Mouse, Tree Boa;    Birds:- Quail, Many kinds of Storks, Bustards, Bishop Finch, Weaver bird, Gulls, Yellow Sparrow, Bittern, Coot, Moor Hen, Teal Duck, Starling, Grey Heron, Little Egret, Plover, Purple Heron, Green Back Purple Coot, Barbet, Bunting, Cuckoo, Francolin, Doves of many kinds including turtle doves, Grouse, Eagles, Ospreys, Vultures of many kinds – these are awful looking wretches, Barn Owls, Secretary Bird, Macaw, Hornbill, Peacocks, Falcon, Cockatoo and many Australian Parrots, Ostriches – lovely birds in full plume, Cranes.   We spent over two hours there but did not have time to see everything.  There is some lovely maiden-hair fern about the Zoo garden, I am enclosing a piece for Mum.</p>
            <p class="hand">Dear Al, two of us have been writing quietly in our mess rooms for a while but now some of the other Gympie pests have arrived and are disturbing the peace.  I gave them some chocolate to get out of the way, so now I will resume this letter.  We left the Zoo about sunset and went to Cairo by tram, had a good tea which cost us 7 piastres each and then strolled about to see the sights.  The streets of Cairo are always crowded with people of all nations, and carts, trams, cabs and motor cars are flying about all the time.  We get pestered with boot-blacks and street-sellers but we just have to say “Hemshi” and off they go. It is very interesting to walk about the streets, which run in all directions and we did not notice the time pass last night and had to get a motor car to get back to camp before our time expired which was 11 p.m..</p>
            <p class="hand">Yesterday a jack-knife was issued to every man in the camp and this week we are to get a tin of chocolates each from the Queen.  “God Save the Queen.”  New tunics have arrived which will be issued shortly.  Well, dear, I will conclude this scribble now, I do hope Dad and Mum are in the best of health also all of you.  You know my thoughts are always with you and some day I hope to be with you all again.</p>
            <p class="hand">So far this trip has been like a holiday and a great experience for me.  Best love to all.</p>
