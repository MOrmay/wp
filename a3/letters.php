          <div class="panel">
            <p class="hand" style="text-align: right;">September 7th. 1914.</p>
            <p class="hand">It is Saturday morning and nearly all our Coy. are half dead from the effects of inoculation which took place yesterday afternoon. It is absolutely deadly. It is nothing for two or three fellows to drop clean out to it while on parade without any warning whatever. We are just beginning to feel the vaccination and were inoculated on the same arm and have to wear that useful member in a sling. It effects one all over  -  head, back and everywhere else.  Just after writing home on Thursday night, the parcel of eatables was brought to me. It was just the thing and the chaps in our tent were talking of writing to Mrs. Baker and telling her what they thought of her. Fancy that. We still have some cake and coconut ice left. Yesterday was pay-day although it should have been last Monday but the pay sheet was not ready. I started an account in the Commonwealth Savings Bank, there is a branch here in camp, and I have arranged for 2/6 (Two Shillings and Six Pence) a day to be placed to my credit while we are away. We do not know when we will leave here but think it will be soon. We will all be allowed to have our vote today.  Breakfast is ready so I will close with love to you all at home.</p>
          </div>
          <button class="accordian">October 24th Albany</button>
          <div class="panel">
            <p class="hand" style="text-align: right;">October 24th. 1914.</p>
            <p class="hand">Trying to write a few lines while we are resting in Albany.  We landed in port from the “Omrah” about six o’clock this morning  -  were taken ashore in a tug as the “Omrah” could not get into the wharf.  We have been marching all round the town and now are having dinner on the side of a nice shady road.  Albany is a very pretty little place and very hilly.  Great big boulders stick out everywhere in the most unlikely places.</p>
            <p class="hand">There are about 4,000 people here and 4 policemen to look after them. When marching through the streets we caused great excitement among the people, and got a good reception  -  they were giving us flowers and fruit along the route and one publican came out with a keg of beer and gave to the troops.  Do not know how long we will be here.  There will be 42 ships in the fleet going home, including the escort.  They will go in three lines with one battleship in front, one on either side and one at back.   Love from Ray.</p>
